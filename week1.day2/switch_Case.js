let browserName = "Chrome";

switch (browserName) {
    case "Chrome":
        console.log("Google Chrome is the best browser in the world.");
        break;
    case "Firefox":
        console.log("Mozilla Firefox is the best browser in the world.");
        break;
    case "Safari":
        console.log("Apple Safari is the best browser in the world.");
        break;
    case "Edge":
        console.log("Microsoft Edge is the best browser in the world.");
        break;
    case "Opera":
        console.log("Opera is the best browser in the world.");
        break;
    default:
        console.log("This browser is not supported.");
        break;
}
// //The break statement is used to exit the switch case statement. If the break statement is not used, the code inside the next case will also be executed

